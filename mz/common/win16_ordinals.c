// Dispatch table
static const Win16OrdTable WIN16_ORD_TABLES[] = {
    {"KERNEL", WIN16_KERNEL_ORD, rd_count_of(WIN16_KERNEL_ORD)},
    {"KRNL286", WIN16_KERNEL_ORD, rd_count_of(WIN16_KERNEL_ORD)},
    {"KRNL386", WIN16_KERNEL_ORD, rd_count_of(WIN16_KERNEL_ORD)},
    {"USER", WIN16_USER_ORD, rd_count_of(WIN16_USER_ORD)},
    {"GDI", WIN16_GDI_ORD, rd_count_of(WIN16_GDI_ORD)},
    {"SOUND", WIN16_SOUND_ORD, rd_count_of(WIN16_SOUND_ORD)},
    {"KEYBOARD", WIN16_KEYBOARD_ORD, rd_count_of(WIN16_KEYBOARD_ORD)},
    {"MOUSE", WIN16_MOUSE_ORD, rd_count_of(WIN16_MOUSE_ORD)},
    {NULL, NULL, 0},
};

const char* mz_win16_ordinal_lookup(const char* module, u16 ordinal) {
    for(const Win16OrdTable* t = WIN16_ORD_TABLES; t->module; t++) {
        if(rd_stricmp(t->module, module) != 0) continue;

        for(u16 i = 0; i < t->count; i++) {
            if(t->entries[i].ordinal == ordinal) return t->entries[i].name;
        }

        return NULL; // module matched but ordinal not found
    }

    return NULL; // module not in table
}
